package com.nucleon.entity;

public class ChineseCharacter {
    //字
    private char zi;
    //不带音调的拼音
    private String pinyin;

    public char getZi() {
        return zi;
    }

    public void setZi(char zi) {
        this.zi = zi;
    }

    public String getPinyin() {
        return pinyin;
    }

    public void setPinyin(String pinyin) {
        this.pinyin = pinyin;
    }
}
